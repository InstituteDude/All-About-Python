# Operasi Aritmatika
a=13
b=3
#Operasi +
hasil =a+b
print("Hasilnya adalah >> ",hasil)
#Operasi -
hasil=a-b
print("Hasilnya adalah >> ",hasil)
#Operasi *
hasil=a*b
print("Hasilnya adalah >> ",hasil)
#Operasi /
hasil=a/b
print("Hasilnya adalah >> ",hasil)
#Operasi %
hasil=a%b
print("Hasilnya adalah >> ",hasil)
#Operasi eksponen (pangkat)
hasil=a**b
print("Hasilnya adalah >> ",hasil)
#Operasi Floor division // (sisanya dibulatkan)
hasil=a//b
print("Hasilnya adalah >> ",hasil)
